package bolum2;

public class KarsilastirmaOperatorleri {


    public static void main(String[] args) {


        /*
        == --> eşit mi?
        != --> eşit değil mi?
        > --> büyük mü?
        >= --> büyük veya eşit mi?
        < --> küçük mü?
        <= --> küçük veya eşit mi?

         */
        System.out.println(3==3);
        System.out.println(3==4);
        System.out.println(3!=4);
        System.out.println(4>5);
        System.out.println(4>=5);
        System.out.println(4<5);
        System.out.println(4<=5);
        System.out.println(4<=4);
        System.out.println("mehmet" == "mehmet");
        System.out.println("mehmet" != "mehmet");



    }

}
